#include <iostream>
#include <vector>
#include <string>
#include <map>

// PotionRecipe 클래스: 재료 목록을 vector<string>으로 변경
class PotionRecipe {
public:
    std::string potionName;
    std::vector<std::string> ingredients; // 단일 재료에서 재료 '목록'으로 변경

    // 생성자: 재료 목록을 받아 초기화하도록 수정
    PotionRecipe(const std::string& name, const std::vector<std::string>& ingredients)
        : potionName(name), ingredients(ingredients) {
    }
};

// AlchemyWorkshop 클래스: 레시피 목록을 관리
class AlchemyWorkshop {
private:
    std::vector<PotionRecipe> recipes;

public:
    // addRecipe 메서드: 재료 목록(vector)을 매개변수로 받도록 수정
    //포인터로 변경
    PotionRecipe* addRecipe(const std::string& name, const std::vector<std::string>& ingredients) {
        recipes.push_back(PotionRecipe(name, ingredients));
        std::cout << ">> 새로운 레시피 '" << name << "'이(가) 추가되었습니다.\n" << std::endl;
        
        return &recipes.back(); //가장 마지막에 넣은 레시피를 반환해서 바로 사용 가능
    }

    // 모든 레시피 출력 메서드
    void displayAllRecipes() const {
        if (recipes.empty()) {
            std::cout << "아직 등록된 레시피가 없습니다." << std::endl;
            return;
        }

        std::cout << "\n--- [ 전체 레시피 목록 ] ---" << std::endl;
        for (size_t i = 0; i < recipes.size(); ++i) {
            std::cout << "- 물약 이름: " << recipes[i].potionName << std::endl;
            std::cout << "  > 필요 재료: ";

            // 재료 목록을 순회하며 출력
            for (size_t j = 0; j < recipes[i].ingredients.size(); ++j) {
                std::cout << recipes[i].ingredients[j];
                // 마지막 재료가 아니면 쉼표로 구분
                if (j < recipes[i].ingredients.size() - 1) {
                    std::cout << ", ";
                }
            }
            std::cout << std::endl;
        }
        std::cout << "---------------------------\n" << std::endl;
    }

    //물약 이름으로 찾기
    //포인터로 변경
    PotionRecipe* searchRecipeByName(const std::string& name)
    {
        for (auto& recipe : recipes)
        {
            if (recipe.potionName == name)
            {
                return &recipe;
            }
        }

        return nullptr;
    }

    //물약 재료 이름으로 검색, 재료가 포함된 모든 물약 찾기
    std::vector<PotionRecipe> searchRecipeByIngredient(const std::string& ingredient)
    {
        std::vector<PotionRecipe> result;

        for (PotionRecipe n : recipes)
        {
            for (std::string i : n.ingredients)
            {
                if (i == ingredient)
                {
                    result.push_back(n);
                    break;
                }
            }
        }

        return result;
    }

    //모든 레시피를 벡터로 반환
    const std::vector<PotionRecipe>& getAllRecipes() { return recipes; }

};

class StockManager
{
private:
    std::map<std::string, int> potionStock;
    const int Max_Stock = 3;

public:
    void initializeStock(std::string potionName)
    {
        potionStock[potionName] = Max_Stock; //포션 레시피 추가시 기본으로 3개 추가 
    }

    bool dispensePotion(const std::string& potionName) {
        auto it = potionStock.find(potionName); // 키 검색

        if (it != potionStock.end())            //포션 검색
        {           
            if (it->second > 0)                 // 재고 확인
            {               
                std::cout <<"\n"<< potionName << " 포션 1개 지급\n" << std::endl;
                it->second--;                    // 재고 감소
                return true;
            }
            else 
            {
                std::cout << "\n포션의 갯수가 부족합니다.\n" << std::endl;
                return false;
            }
        }
        else 
        {
            std::cout << "\n해당 포션이 존재하지 않습니다.\n" << std::endl;
            return false;
        }
    }


    void returnPotion(std::string potionName) //지급한 물약 공병 받기, 지급한 포션의 갯수를 저장하고 함수 호출해서 돌려 받기?
    {
        auto it = potionStock.find(potionName);
        if (it != potionStock.end()) {
            if (it->second < Max_Stock) { // 재고가 최대치보다 작으면
                it->second++;
                std::cout << "\n" << potionName << " 포션 1개 반환 완료\n";
                std::cout << "남은 포션의 개수: " << potionStock[potionName] <<"\n"<< std::endl;
            }
            else {
                std::cout <<"\n"<< potionName << " 포션의 재고가 이미 최대치입니다. 반환 불가.\n" << std::endl;
            }
        }
        else {
            std::cout << "해당 포션이 존재하지 않아 반환할 수 없습니다.\n" << std::endl;
        }
    }

    int getStock(std::string potionName) //물약 갯수 확인
    {
        return potionStock[potionName];
    }
};


int main() {
    AlchemyWorkshop myWorkshop;
    StockManager stockManager;

    while (true) {
        std::cout << "**연금술 공방 관리 시스템**" << std::endl;
        std::cout << "1. 레시피 추가" << std::endl;
        std::cout << "2. 모든 레시피 출력" << std::endl;
        std::cout << "3. 이름으로 검색" << std::endl;
        std::cout << "4. 재료 이름으로 검색" << std::endl;
        std::cout << "5. 재고 검색" << std::endl;
        std::cout << "6. 물약 지급" << std::endl;
        std::cout << "7. 물약 반환" << std::endl;
        std::cout << "8. 종료" << std::endl;
        std::cout << "선택: ";

        int choice;
        std::cin >> choice;

        if (std::cin.fail()) {
            std::cout << "잘못된 입력입니다. 숫자를 입력해주세요." << std::endl;
            std::cin.clear();
            std::cin.ignore(10000, '\n');
            continue;
        }

        if (choice == 1) {
            std::string name;
            std::cout << "물약 이름: ";
            std::cin.ignore(10000, '\n');
            std::getline(std::cin, name);

            // 여러 재료를 입력받기 위한 로직
            std::vector<std::string> ingredients_input;
            std::string ingredient;
            std::cout << "필요한 재료들을 입력하세요. (입력 완료 시 '끝' 입력)" << std::endl;

            while (true) {
                std::cout << "재료 입력: ";
                std::getline(std::cin, ingredient);

                // 사용자가 '끝'을 입력하면 재료 입력 종료
                if (ingredient == "끝") {
                    break;
                }
                ingredients_input.push_back(ingredient);
            }

            // 입력받은 재료가 하나 이상 있을 때만 레시피 추가
            // 재고 추가
            if (!ingredients_input.empty()) 
            {
                PotionRecipe* newRecipe =  myWorkshop.addRecipe(name, ingredients_input);
                stockManager.initializeStock(newRecipe->potionName);
            }
            else {
                std::cout << ">> 재료가 입력되지 않아 레시피 추가를 취소합니다." << std::endl;
            }

        }
        else if (choice == 2) {
            myWorkshop.displayAllRecipes();

        }
        else if (choice == 3) //포션 이름으로 검색
        {
            std::string name;
            std::cout << "\n포션 이름을 입력하세요: ";
            std::cin >> name;
            
            //nullptr 반환시 물약 없음을 출력

            if (myWorkshop.searchRecipeByName(name) == nullptr)
            {
                std::cout << "\n물약이 존재하지 않습니다.\n" << std::endl;
            }
            else
            {
                std::cout << myWorkshop.searchRecipeByName(name)->potionName  << " 물약이 존재합니다\n" << std::endl;
            }
            

        }
        else if (choice == 4)
        {
            std::string ingredient;
            std::cout << "\n재료 이름을 입력하세요: ";
            std::cin >> ingredient;

            std::cout << "\n검색한 재료를 포함하는 물약: ";

            for (PotionRecipe n : myWorkshop.searchRecipeByIngredient(ingredient))
            {
                std::cout << n.potionName << " ";
            }
            std::cout << "\n" << std::endl;
        }
        else if (choice == 5) //재고 검색
        {
            std::string name;

            std::cout << "\n검색할 물약 이름: ";
            std::cin >> name;

            std::cout << "\n물약 이름: " << name << "\n재고: " << stockManager.getStock(name) <<"\n" << std::endl;
        }
        else if (choice == 6) //물약 지급
        {
            std::string name;

            std::cout << "지급할 물약을 선택하세요: ";
            std::cin >> name;

            if (stockManager.dispensePotion(name))
            {
                std::cout << "포션 지급 성공\n";
                std::cout << "남은 포션의 개수: " << stockManager.getStock(name) <<"\n"<< std::endl;
            }
            else
            {
                std::cout << "포션 지급 실패\n" << std::endl;
            }
        }
        else if (choice == 7) //물약 반환
        {
            std::string name;

            std::cout << "반환할 물약을 선택하세요: ";
            std::cin >> name;

            stockManager.returnPotion(name);
        }
        else if (choice == 8) {
            std::cout << "공방 문을 닫습니다..." << std::endl;
            break;

        }
        else {
            std::cout << "잘못된 선택입니다. 다시 시도하세요." << std::endl;
        }
    }

    return 0;
}